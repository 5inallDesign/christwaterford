<h1>What if I'm not Lutheran?</h1>
        
<p>Everybody is welcome at Christ Lutheran. For the most part, we use standard, easy-to-follow Lutheran liturgy in our services. The words are printed in your bulletin and projected onto a large screen during the service.</p>

<p>We have an open communion policy that welcomes everyone to the Lord's table.</p>

<h2>What do you do beside Worship?</h2>

<p>We're big on community service. Our doors are open to organizations like:</p>
<ul>
	<li>AA</li>
    <li>TOPS</li>
    <li>Red Cross Blood Drive</li>
    <li>Waterford Historical Society</li>
</ul>

<p>We actively participate in:</p>
<ul>
	<li>Habitat for Humanity</li>
    <li>CROP Walk for Hunger</li>
    <li>Community-wide Good Friday services</li>
    <li>Community projects through WELCA, our active women's group</li>
    <li>A gigantic yard sale every other year</li>
</ul>

<p>We enjoy each other's company and join together during the year for:</p>
<ul>
	<li>A chili cook off</li>
    <li>Euchre tournaments</li>
    <li>A Halloween party</li>
    <li>A Peace Garden that grows vegetables for needy area families</li>
</ul>

<p>Our Adult Discussion Group meets once a month to discuss Christianity in a changing world. We're studied comparative religions and explored the writings and ideas of contemporary Christians thinkers like Marcus Borg, Amy Jill Levine, and John Shelby Spong.</p>

<p>And we're always looking for new things to do. Maybe you have some ideas.</p>

