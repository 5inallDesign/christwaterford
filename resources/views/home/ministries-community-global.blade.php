<h1>Community and Global Team</h1>

<p>We seek to share the love of God by serving those in need both locally and around the world. We strive to be advocates for God's justice and good stewards of God's creation.</p>

<p>A Creation Care Task Force has been formed as part of our Community and Global 
  Ministry team. This task force has the following purpose statement: "To share the love
  of God in Christ by helping the congregation increase its awareness of environmental
  concerns, and by providing practical ways to address those concerns through the 
  congregation’s ministry and within our community."</p>

<p>Since its beginning this Task Force has sponsored an “Earth Day Worship Service
  Celebration” and has planted a community garden on the church property which
  has come to be known as the “Christ Lutheran Church Peace Garden.”  Vegetables 
  grown in the community garden will be given to local food depots, as well as 
  members and those in need of food. The Creation Care Task Force is lead by 
  Jamie Schutt. For more info please use the contact links on this website.</p>

